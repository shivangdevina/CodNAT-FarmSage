@dataclass
class Chunk:
    doc_id: str
    chunk_id: str
    text: str
    metadata: Dict[str, Any]
    embedding: Optional[List[float]] = None # Add optional embedding field


def chunk_documents(documents: List[Document], chunk_size: int = 500, overlap: int = 50) -> List[Chunk]:
    chunks: List[Chunk] = []
    for doc in documents:
        if not doc.text:
            continue
        words = doc.text.split()
        start = 0
        chunk_index = 0
        while start < len(words):
            end = start + chunk_size
            text_segment = " ".join(words[start:end])
            chunk_metadata = {
                "doc_id": doc.id,
                "uri": doc.uri,
                "source": doc.metadata.get("source"),
                "page": doc.metadata.get("page", None),
                #"timestamp": datetime.utcnow().isoformat()
                "timestamp": datetime.datetime.now(timezone.utc).isoformat(),

            }
            chunk = Chunk(
                doc_id=doc.id,
                chunk_id=f"{doc.id}_{chunk_index}",
                text=text_segment,
                metadata=chunk_metadata
            )
            chunks.append(chunk)
            chunk_index += 1
            start += chunk_size - overlap
    logger.info(f"Created {len(chunks)} chunks from {len(documents)} documents")
    return chunks

