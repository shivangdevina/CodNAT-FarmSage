def collect_documents(data_dir: Path) -> List[Document]:
    docs: List[Document] = []
    for file_path in data_dir.glob("**/*"):
        if file_path.suffix.lower() in [".pdf", ".docx", ".txt", ".md"]:
            doc = Document(
                id=file_path.stem,
                uri=str(file_path),
                metadata={},
                blob_path=file_path
            )
            docs.append(doc)
    logger.info(f"Collected {len(docs)} documents from {data_dir}")
    return docs

